/* what are the four pillars of OOP

1) ABSTRACTION: bir objenin kullanıcı için gereksiz kısımlarını gizleyip
gerekli olan kısmı görünür kılmaya denir.

2) ENCAPSULATION: değişkenleri (attributes) ve fonksiyonları (methods)
bir objenin eti ve kemiği olarak düşünebiliriz. bu ikisini bir arada tutmaya
encapsulation denir. etkileşim halindedirler. mesela bir objenin bütün
değişkenlerine ulaşamayabilir bir kullanıcı ama getX() gibi metotlarla ulasabilir.

3) INHERITANCE: bir objenin başka bir objeden özelliklerini almasıdır bir nevi miras.
üstüne kendi de bir şeyler ekleyebilir ya da aldığı şeyi override ile değiştirebilir ama
hiçbir zaman parentda da kullanılamayacak şekilde silemez.

4) POLYMORPHISM: özellikle virtual + inheritance kullanımında ortaya çıkar. türkçesi çok biçimlilik.
base sınıfının bir metodunu override anahtar kelimesiyle derived sınıfında değiştirebilirsin */
